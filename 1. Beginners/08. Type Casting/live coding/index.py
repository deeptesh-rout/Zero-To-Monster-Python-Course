# int()
float_num = 3.14
int_num = int(float_num)
print(int_num)  # 3

# float()
int_num = 5
float_num = float(int_num)
print(float_num)  # 5.0

# str()
num = 42
str_num = str(num)
print(str_num)  # '42'

# bool()
num = 0
bool_value = bool(num)
print(bool_value)  # False

# ord() and chr()
char = 'A'
unicode_code = ord(char)
print(unicode_code)  # 65

new_char = chr(65)
print(new_char)  # 'A'


