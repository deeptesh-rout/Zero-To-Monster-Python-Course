# Iterating Through Keys
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for key in my_dict:
    print(key)

# Iterating Through Values
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for value in my_dict.values():
    print(value)

# Iterating Through Key-Value Pairs
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for key, value in my_dict.items():
    print(f"{key}: {value}")

# items() Method
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for key, value in my_dict.items():
    print(f"{key}: {value}")

# keys() Method
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for key in my_dict.keys():
    print(key)

# values() Method
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
for value in my_dict.values():
    print(value)
