# append
my_list = [1, 2, 3]
my_list.append(4)

# extend
my_list = [1, 2, 3]
my_list.extend([4, 5, 6])

# insert
my_list = [1, 2, 3]
my_list.insert(1, 4)

# remove
my_list = [1, 2, 3, 2]
my_list.remove(2)

# pop
my_list = [1, 2, 3]
popped_element = my_list.pop(1)

# count
my_list = [1, 2, 3, 2]
count_of_2 = my_list.count(2)

# sort
my_list = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
my_list.sort()

# reverse
my_list = [1, 2, 3]
my_list.reverse()

# copy
original_list = [1, 2, 3]
copied_list = original_list.copy()
